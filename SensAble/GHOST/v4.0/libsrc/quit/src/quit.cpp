static GLuint display_list;

/***************************
**:::create_quit_button:::**
****************************
**Create the display list for the quit button. */
void create_quit_button(float width)
{
	GLfloat button_mat_amb_diff[] = {0.65, 0.65, 0.65, 1.0};
	display_list = glGenLists(1);
	glNewList(display_list, GL_COMPILE);
	glMaterialfv(GL_FRONT, GL_AMBIENT_AND_DIFFUSE, button_mat_amb_diff);
	draw_half_cube (width);
	glEndList();
}

/*************************
**:::draw_quit_button:::**
**************************
**Draw the quit button. */
void draw_quit_button()
{
	glCallList(display_list);
}

/***********************
**:::draw_half_cube:::**
************************
**Draw a half cube.
This minor optimization only draws the top half (5 sides) of a cube,
with width specified centered at origin.  It does not draw the back,
which is the side parallel to z-axis farthest from viewer. */
static void draw_half_cube(float width)
{
  glBegin(GL_QUADS);
  /* left */
  glNormal3f(-1.0, 0.0, 0.0);
  glVertex3f(-width/2.0, -width/2.0, 0.0);
  glVertex3f(-width/2.0, -width/2.0, width/2.0);
  glVertex3f(-width/2.0, width/2.0, width/2.0);
  glVertex3f(-width/2.0, width/2.0, 0.0);
  /* right */
  glNormal3f(1.0, 0.0, 0.0);
  glVertex3f(width/2.0, -width/2.0, 0.0);
  glVertex3f(width/2.0, width/2.0, 0.0);
  glVertex3f(width/2.0, width/2.0, width/2.0);
  glVertex3f(width/2.0, -width/2.0, width/2.0);
  /* top */
  glNormal3f(0.0, 1.0, 0.0);
  glVertex3f(-width/2.0, width/2.0, 0.0);
  glVertex3f(-width/2.0, width/2.0, width/2.0);
  glVertex3f(width/2.0, width/2.0, width/2.0);
  glVertex3f(width/2.0, width/2.0, 0.0);
  /* bottom */
  glNormal3f(0.0, -1.0, 0.0);
  glVertex3f(-width/2.0, -width/2.0, 0.0);
  glVertex3f(width/2.0, -width/2.0, 0.0);
  glVertex3f(width/2.0, -width/2.0, width/2.0);
  glVertex3f(-width/2.0, -width/2.0, width/2.0);
  glEnd();
  
  glEnable(GL_TEXTURE_2D);
  glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
  glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP);
  glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP);
  glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
  glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
  glTexEnvf(GL_TEXTURE_ENV, GL_TEXTURE_ENV_MODE, GL_DECAL);
  glTexImage2D(GL_TEXTURE_2D, 0, 3, QUIT_TEXTURE_SIZEX, 
	       QUIT_TEXTURE_SIZEY, 0, GL_RGB, GL_UNSIGNED_BYTE, 
	       quit_texture_data);
  glBegin(GL_QUADS);
  /* front  */
  glNormal3f(0.0, 0.0, 1.0);
  glTexCoord2f(0.0, 0.0); glVertex3f(-width/2.0, -width/2.0, width/2.0);
  glTexCoord2f(1.0, 0.0); glVertex3f(width/2.0, -width/2.0, width/2.0);
  glTexCoord2f(1.0, 1.0); glVertex3f(width/2.0, width/2.0, width/2.0);
  glTexCoord2f(0.0, 1.0); glVertex3f(-width/2.0, width/2.0, width/2.0);
  glEnd();
  glDisable(GL_TEXTURE_2D);
}

/***************************
***::updateQuitButtonCB:::**
****************************
**Graphics update callback for the quit button.
**[*objH] is the quit button dynamic.
**[*] is the cbData structure.
**[*param] is the user-defined parameter which is passed as an 
           argument to the function when it is called.
	   In this case, it is the pointer to the graphics button 
	   position.
Updates the graphics button to the position of the haptics
button.  This is processed whenever scene->updateGraphics is called. */
void updateQuitButtonCB(gstTransform *objH, void *, void *param) {
  gstButton *button = (gstButton *)objH;
  gstPoint *position = (gstPoint *)param;
  gstTransform *parentSep =  button->getParent();
  gstPoint haptic_position = button->getPosition()+parentSep->getPosition();
  /* Equivalently:
     | gstPoint haptic_position = button->getWorldPosition(); 
     */
  *position = haptic_position;
}

/**************************
**:::eventQuitButtonCB:::**
***************************
**Event callback for the quit button.
**[*] is the quit button dynamic.
**[*eventData] is the gstEvent callback structure, which contains 
            information about the dynamic object.  For a gstButton,
	    the event.id contains the state of the button.
**[*param] is the user-defined parameter which is apssed as an
           argument to the function when it is called.
           In this case, it is the pointer to the scene.
Stops the servo loop when the button is pressed.  This
function is called by scene->updateEvents whenever
the button state changes. */
void eventQuitButtonCB(gstTransform *, void *eventData, void *param) {
  gstEvent *event = (gstEvent *)eventData;
  gstScene *scene = (gstScene *)param;
  if (event->id == gstButton::PRESSED)
    scene->stopServoLoop();
}

/****************************
**:::create_quit_buttonH:::**
*****************************
**Creates the haptic quit button.
**[width] is the width of the button.
**[&position]  is the position of the button.
**[*scene] is the scene node of the haptics scene.
This function creates the quit button.  The quit button
is set with an event callback to stop the servoloop when
the button is pressed.  The scene graph for the button is:
>> gstSeparator->gstDynamic->gstCube <<
*/
gstSeparator *create_quit_buttonH(float width, gstPoint &position, gstScene *scene) {
  /* Create the button separator. This contains the global position and
     orientation of the button. */
  gstSeparator *buttonSep = new gstSeparator;
  buttonSep->setPosition(position);
  /* Create the button dynamic.  The button dynamic specifies the behavior
     of the button. */
  gstButton *buttonDynamic = new gstButton;
  /* Specify the properties of the button.  
     This is the resistance to velocity. Setting a high damping
     coefficient might give the feel of pushing the button through
     jelly. */
  buttonDynamic->setDamping(2.0);
  /* Set the mass of the button.  
     This controls the acceleration of the button when it is pressed. */
  buttonDynamic->setMass(.02);
  /* Set the spring stiffness of the button.  
     This determines how much force must be applied to move the button. */
  buttonDynamic->setK(150);
  /* Set the deadband ofaddChild the button.
     This is the region where the restoring spring force is absent.
     Physically, it is the region after the button "clicks" when it is
     pressed. */
  buttonDynamic->setDeadband(width/2-.1);
  /* Set the throw distance of the button.
     This is the length of travel for the button.  It is the distance that
     the user can push the button. */
  buttonDynamic->setThrowDist(width/2);
  /* Create the button cube.  This is the actual haptics object that the
     user interacts with.  Any object can be made into a button; a button
     dynamic can, for example, be attached to a separator so that the entire
     scene graph underneath the separator behaves as a single button. */
  gstCube *buttonCube = new gstCube;
  /* Equivilantly:
     | buttonCube->setHeight(width/2);
       buttonCube->setWidth(width/2);
       buttonCube->setDepth(width/2);
       */
  buttonCube->setScale(width/2,width/2,width/2);
  buttonSep->addChild(buttonDynamic);
  buttonDynamic->addChild(buttonCube);
  /* Set the callbacks for the button.  There are two types of callbacks:
     graphic and event.
     ** Graphics callbacks are triggered whenever the object moves.  They
        are to notify the application that the position or orientation has
	changed.  A graphics callback will normally update the positon of
	the graphics equivilant of the object.
	In the case of the quit button, the graphics representation is the 
	GL half-cube.  Whenever the haptics button is moved, the callback 
	updates the position of the button so that it is drawn at its new 
	position. 
	Whenever scene->updateGraphics is executed, the scene triggers
	graphic callbacks for any objects which have moved.
     ** Event callbacks are triggered whenever a dynamic object's state
        changes.  Dynamic objects are normally associated with actions, so 
	event callbacks are necessary to inform the application to perform
	that action. 
	In this case, the quit button acts to stop the servo loop and quit
	from the applicaton when it is pressed.  The event callback is 
	automatically triggered when the button is pressed, and the callback
	code stops the servo loop.
	Whenever scene->updateEvents is executed, the scene triggers
	event callbacks for any objects whose states have changed.  For
	example, an event is generated when a button goes from unpressed
	to pressed, or a dial rotates to a new notch.
	*/

  /* Add graphics callback so we can see the button move. */
  buttonDynamic->setGraphicsCallback(updateQuitButtonCB,&position);
  /* Add event callback to stop the servo loop when the
     button is pressed. */
  buttonDynamic->setEventCallback(eventQuitButtonCB,scene);
  return buttonSep;
}

